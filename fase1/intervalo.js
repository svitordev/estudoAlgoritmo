function intervalorEntreValores(num){
    if(num >= 10 && num <= 20){
        console.log(`${num} está dentro do intervalo entre 10 e 20.`)
    } else {
        console.log(`${num} não está dentro do intervalo entre 10 e 20.`)
    }
}
intervalorEntreValores(4);
intervalorEntreValores(10);
intervalorEntreValores(20);
intervalorEntreValores(24);